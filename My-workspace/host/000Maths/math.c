int math_add(int value1, int value2){
	return value1+value2 ;
}
int math_sub (int value1, int value2){
	return value1-value2;
}
long long int math_mul (int value1, int value2){
	return (long long int )value1*value2;
}
float math_div (int value1, int value2){
	return (float )value1/value2;
}
